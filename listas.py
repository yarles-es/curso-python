# Tipo List - Mutável

# Métodos úteis:

# append() - Adiciona um elemento no final da lista
# insert() - Adiciona um elemento em uma posição específica
# pop() - Remove um elemento da lista
# del() - Remove um elemento da lista
# remove() - Remove um elemento da lista
# sort() - Ordena a lista
# reverse() - Inverte a lista
# index() - Retorna o índice de um elemento
# count() - Retorna a quantidade de elementos
# len() - Retorna a quantidade de elementos
# copy() - Retorna uma cópia da lista
# clear() - Limpa a lista
# extend() - Adiciona elementos de uma lista em outra
# + - Concatena listas
